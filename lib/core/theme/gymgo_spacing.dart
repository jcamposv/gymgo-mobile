/// GymGo Spacing System
/// Consistent spacing values across the app
class GymGoSpacing {
  GymGoSpacing._();

  // Base unit: 4px
  static const double unit = 4.0;

  // Spacing scale
  static const double xxs = 4.0;   // unit * 1
  static const double xs = 8.0;    // unit * 2
  static const double sm = 12.0;   // unit * 3
  static const double md = 16.0;   // unit * 4
  static const double lg = 24.0;   // unit * 6
  static const double xl = 32.0;   // unit * 8
  static const double xxl = 48.0;  // unit * 12
  static const double xxxl = 64.0; // unit * 16

  // Screen padding
  static const double screenHorizontal = 24.0;
  static const double screenVertical = 16.0;

  // Card padding
  static const double cardPadding = 20.0;
  static const double cardPaddingLarge = 24.0;

  // Input padding
  static const double inputHorizontal = 16.0;
  static const double inputVertical = 16.0;

  // Button padding
  static const double buttonHorizontal = 24.0;
  static const double buttonVertical = 16.0;

  // Border radius
  static const double radiusXs = 4.0;
  static const double radiusSm = 8.0;
  static const double radiusMd = 12.0;
  static const double radiusLg = 16.0;
  static const double radiusXl = 24.0;
  static const double radiusFull = 999.0;

  // Icon sizes
  static const double iconSm = 16.0;
  static const double iconMd = 20.0;
  static const double iconLg = 24.0;
  static const double iconXl = 32.0;
}
